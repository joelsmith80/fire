              <section id="paraphrasing" class="sticky-step region entry-content">
                <a href="#" class="js-close-link js-close-link-top">Close</a>
                <header class="section-header">
                  <h2 class="section-title">5. Paraphrasing</h2>
                </header>
                <div class="main">
                  <p>The paraphrase can be considered the most nuanced and complex tool we have as facilitators. As we begin the practice of facilitation, it is helpful to identify the specific objectives of the paraphrase. When you paraphrase, you:</p>
                  <ul>
                    <li>ensure that everyone in the group hears each idea</li>
                    <li>underscore the fact that listening to and trying to understand others is important</li>
                    <li>indicate that you have not just heard but also understood them, encouraging participants to engage</li>
                    <li>build participants' sense of being valued and capable</li>
                    <li>model clearer, crisper language, grammar, and vocabulary</li>
                    <li>access authentic, student-centered perspectives</li>
                  </ul>
                  <p>As a part of teaching critical thinking, you need to emphasize the importance of considering all possibilities. The VTS process allows students to find the interpretations that mean the most to them, thus encouraging further learning.</p>
									<div class="pop-open dig-deeper">
                    <h3 class="title dig-deeper-title trigger dig-deeper-trigger" data-action="trigger"><span>Paraphrasing with conditional language</span></h3>
                    <div class="dig-deeper-main" data-action="reveal">
											<p>Conditional Language is a tool that supports the environment of VTS that ensures and encourages multiple perspectives and engagement by all. Rephrase ideas as conditional. For example:</p>
	                   <ul>
	                     <li>"You think this could be..."</li>
	                     <li>"Noah is considering the possibility that..."</li>
	                     <li>"For Maria, this might be taking place on a farm..."</li>
	                   </ul>
                    </div>
									</div>
									<div class="pop-open dig-deeper">
                    <h3 class="title dig-deeper-title trigger dig-deeper-trigger" data-action="trigger"><span>Paraphrasing with linking</span></h3>
                    <div class="dig-deeper-main" data-action="reveal">
											<p>Working together in VTS discussions, participants can solve complex problems and decipher meaning from the artwork. Peer interactions lead to more observation, more ideas, and usually greater accuracy. Debating and building on the ideas of each other provides an opportunity to stretch one's reasoning skills.</p>
											<p>In order to encourage this behavior of critical analysis and response to others' ideas, facilitators should keep track of various strands of thought and draw links among them while paraphrasing. This can mean pointing out when comments are in agreement or opposition, as well as elaborations or revisions.</p>
											<p>When the facilitator links various ideas, creating a kind of outline of the discussion as it builds, participants become aware of how thinking unfolds and how meanings are discovered.</p>
										</div>
									</div>
                  <div class="pop-open dig-deeper">
                    <h3 class="title dig-deeper-title trigger dig-deeper-trigger" data-action="trigger"><span>Paraphrase with framing</span></h3>
                    <div class="dig-deeper-main" data-action="reveal">
											<p>In more advanced paraphrasing, we include the tool of framing ideas and thinking for the benefit of the group as a whole. Framing involves beginning paraphrases with language that contextualizes the comments and makes clear the lens they are using to approach the image. Notice and identify how a student is processing the information and ideas presented in the discussion.</p>
											<p>Possible categories for the frame include:</p>
											<p><b>Identifying the big idea in a comment</b></p>
											<ul>
												<li>"You are identifying a <i>time period</i>..."</li>
												<li>"Amy is talking about the <i>setting</i>, about where this could be taking place..."</li>
												<li>"You are reading this detail as a <i>symbol</i> of..."</li>
												<li>"Malik is noticing a <i>pattern</i>..."</li>
												<li>"Astrid is examining the <i>composition</i> of this work..."</li>
											</ul>
											<p><b>Pinpointing the location that the comment is referring to</b></p>
											<ul>
												<li>"You are <i>looking to the right</i> of the girl..."</li>
												<li>"You are <i>drawing our attention to the foreground</i>..."</li>
											</ul>
											<p><b>Identifying metacognition</b></p>
											<ul>
												<li>"You are <i>inferring</i> that this..."</li>
												<li>"You are <i>wondering</i> about..."</li>
												<li>"You are <i>asking a question</i>..."</li>
												<li>"You are <i>elaborating</i> on Michael's idea..."</li>
											</ul>
                    </div>
                  </div>
									<p><i>Looking for an opportunity to refine your paraphrasing skills? Join us for <a href="/training">our practicum series</a>.</i></p>
