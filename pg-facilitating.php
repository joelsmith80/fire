                  <h2 class="section-title">2. Silent Looking</h2>
                </header>
                <div class="main">
                  <p>Begin the VTS session by inviting your group to look at the image silently, making sure that everyone has an unobstructed view. This silent moment allows participants to begin the process of observing, inferring, and meaning-making. The silent moment should be long enough for the group to read the entire image — about 30 seconds. Facilitators should use this time to both observe the image and reflect on their own practice.</p>
