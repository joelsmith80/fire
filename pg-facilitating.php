              <section id="three-questions" class="sticky-step region entry-content">
                <a href="#" class="js-close-link js-close-link-top">Close</a>
                <header class="section-header">
                  <h2 class="section-title">3. The Three Questions</h2>
                </header>
                <div class="main">
                  <p>Facilitators ask only three questions. Fidelity to these questions and the wording is critical to developing the habits of mind necessary for independent critical thinking, and skilled communication.</p>
                  <ol>
                    <li>
                      <h3>1. What's going on in this picture?</h3>
                      <p>This question opens the conversation. We ask it only once. The phrasing suggests the image is about something. It encourages storytelling and the opportunity for participants to think and speak for themselves.</p>
											<p>For pre-kindergarten through first grade during the first few VTS lessons, you may choose to ask the question: "What do you <em>see</em> in this picture?"</p>
											<p>During the first few VTS lessons, ask the question, "What do you see in this picture?" This question meets young viewers in a developmentally appropriate way. Once you begin to hear students infer things from an image — going beyond observations such as "I see a girl" to comments like "I think she is sad" — initiate the discussions with "What's going on in this picture" – a question that drives this more sophisticated way of meaning-making.</p>
                    </li>
                    <li>
                      <h3>2. What do you see that makes you say ...?</h3>
                      <p>This question asks participants to look more and gather evidence to support ideas or interpretations. Finding evidence in the image supports the skill of fact-based reasoning and debate in any context.</p>
                      <p>Ask this question whenever you hear an inference, even if you agree with the opinion or you think you know what the participant refers to. In this question, VTS provides the opportunity for us to articulate and examine the "why" behind our assumptions and observations. We only ask this question once per student interaction. One follow-up question per student maintains neutrality and honors the goal of hearing as many voices as possible in a discussion.</p>
                    </li>
                    <li>
                      <h3>3. What more can you find?</h3>
                      <p>We ask this question after each paraphrase to encourage thorough examination of the image. This question is a very direct way of pushing beyond early impressions. It has the effect of making the conversation richer and more complete. Long term, the repetition of "What more can you find?" supports perseverance. Research shows that the capacity to probe — to move beyond what is obvious — needs to be nurtured to grow.</p>
                    </li>
                  </ol>
                </div>
